package com.cinnamon.object;

/**
 * Describes an object which has an inherent width and height.
 *
 *
 */
public interface Dimensional
{
    /**
     * <p>Gets the width.</p>
     *
     * @return width.
     */
    float getWidth();

    /**
     * <p>Sets the width.</p>
     *
     * @param width width.
     */
    void setWidth(float width);

    /**
     * <p>Gets the height.</p>
     *
     * @return height.
     */
    float getHeight();

    /**
     * <p>Sets the height.</p>
     *
     * @param height height.
     */
    void setHeight(float height);
}

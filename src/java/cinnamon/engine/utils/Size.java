package cinnamon.engine.utils;

/**
 * <p>Read-only access to width, height, and depth properties.</p>
 */
public interface Size
{
    /**
     * <p>Gets the width.</p>
     *
     * @return width.
     */
    float getWidth();

    /**
     * <p>Gets the height.</p>
     *
     * @return height.
     */
    float getHeight();

    /**
     * <p>Gets the depth.</p>
     *
     * @return depth.
     */
    float getDepth();
}

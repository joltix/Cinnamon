package cinnamon.engine.utils;

/**
 * <p>Describes a location in three dimensions.</p>
 */
public interface Position
{
    /**
     * <p>Gets the x position.</p>
     *
     * @return x.
     */
    float getX();

    /**
     * <p>Gets the y position.</p>
     *
     * @return y.
     */
    float getY();

    /**
     * <p>Gets the z position.</p>
     *
     * @return z.
     */
    float getZ();
}